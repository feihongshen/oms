package cn.explink.b2c.Wholeline;

public class WholeLine {
	public String url;
	public String method;
	public String comeCode;
	public String password;
	public String usercode;
	public String expt_code;

	public String getUsercode() {
		return usercode;
	}

	public void setUsercode(String usercode) {
		this.usercode = usercode;
	}

	public String getUrl() {
		return url;
	}

	public void setUrl(String url) {
		this.url = url;
	}

	public String getMethod() {
		return method;
	}

	public void setMethod(String method) {
		this.method = method;
	}

	public String getComeCode() {
		return comeCode;
	}

	public void setComeCode(String comeCode) {
		this.comeCode = comeCode;
	}

	public String getPassword() {
		return password;
	}

	public void setPassword(String password) {
		this.password = password;
	}

	public String getExpt_code() {
		return expt_code;
	}

	public void setExpt_code(String expt_code) {
		this.expt_code = expt_code;
	}

}
