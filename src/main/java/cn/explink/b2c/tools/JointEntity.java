package cn.explink.b2c.tools;

public class JointEntity {

	private int joint_num;
	private String joint_property;
	private int state;
	private String joint_remark;

	public int getJoint_num() {
		return joint_num;
	}

	public void setJoint_num(int joint_num) {
		this.joint_num = joint_num;
	}

	public String getJoint_property() {
		return joint_property;
	}

	public void setJoint_property(String joint_property) {
		this.joint_property = joint_property;
	}

	public String getJoint_remark() {
		return joint_remark;
	}

	public void setJoint_remark(String joint_remark) {
		this.joint_remark = joint_remark;
	}

	public int getState() {
		return state;
	}

	public void setState(int state) {
		this.state = state;
	}

}
